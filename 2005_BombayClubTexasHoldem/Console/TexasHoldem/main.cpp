///////////////////////////////////////
#include "config.h"
///////////////////////////////////////

///////////////////////////////////////
using std::cerr;
using std::flush;
using std::exception;
///////////////////////////////////////

///////////////////////////////////////
extern void doMain( );
///////////////////////////////////////

///////////////////////////////////////
int main( )
{
	try
	{
		doMain( );
	}
	catch( const std::exception& e )
	{
		cerr << EXCEPTION << e.what( ) << '!';
	}
	catch( ... )
	{
		cerr << UNKNOWN_ERROR;
	}

	flush( cerr );

	return( EXIT_SUCCESS );
}
